package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		double a = 2.5;
		System.out.println(a);
		
		var b = 4.5; // Java vai inferir o tipo pelo valor que você colocou, que ele é do tipo double.	
		System.out.println(b);
		
		var c = "texto"; // Java definiou que c será do tipo string, não é possível mudar.
		System.out.println(c);
		
		// c = 4.5; -> não pode fazer isso
		
		double d; // variável foi declarada
		d = 123.65; // variável foi inicializada
		System.out.println(d); // variável usada!
		
		var e = 123.45; // quando usar VAR é preciso declarar e inicializar,
                        // pois o Java nao conseguirá inferir qual é o tipo de variável
		System.out.println(e);
		
		
	}
}
